// This package holds the global functions.
package utils.globalFunctions

// This function gets the cross sum of the birthdate from the user.
fun getCrossSum(
		number1 : Int,
		number2 : Int,
		number3 : Int,
		number4 : Int,
		number5 : Int,
		number6 : Int,
		number7 : Int,
		number8 : Int,
               ) : Int
{
	return number1 + number2 + number3 + number4 + number5 + number6 + number7 + number8
}
